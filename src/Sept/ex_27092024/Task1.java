package Sept.ex_27092024;
/*
 example of while vs do while loop in Lab ex.
 */
public class Task1 {
    public static void main(String[] args) {
        //example to illustrate Do while loop
        int a = 1;
        do{
            System.out.println("print number once");
            a++;
        } while(a<2);
        //example to illustrate while loop
        int b = 6;
        while (b<10)
        {
            System.out.println(b);
            b++;
        }
    }

}
